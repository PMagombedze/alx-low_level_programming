#include "3-calc.h"

/**
 * op_add - add numbers
 * @a: first number
 * @b: second number
 *
 * Return: answer
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - subtract numbers
 * @a: first number
 * @b: second number
 *
 * Return: answer
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiply numbers
 * @a: first number
 * @b: second number
 *
 * Return: answer
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - divide numbers
 * @a: first number
 * @b: second number
 *
 * Return: answer
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - modulus of numbers
 * @a: first number
 * @b: second number
 *
 * Return: answer
 */
int op_mod(int a, int b)
{
return (a % b);
}
